package space.pxls;

public class Lookup {
    int id;
    int x;
    int y;
    String origin;
    Integer pixelCount;
    Integer pixelCountAlltime;
    long time;
    String username;
    String discordName;
    String faction;

    public int getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getOrigin() {
        return origin;
    }

    public Integer getPixelCount() {
        return pixelCount;
    }

    public Integer getPixelCountAlltime() {
        return pixelCountAlltime;
    }

    public long getTime() {
        return time;
    }

    public String getUsername() {
        return username;
    }

    public String getDiscordName() {
        return discordName;
    }

    public String getFaction() {
        return faction;
    }
}
